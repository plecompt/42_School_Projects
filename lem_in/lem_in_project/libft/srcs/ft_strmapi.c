#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	unsigned int		i;
	unsigned int		len;
	char				*str;

	if (s && f)
	{
		i = 0;
		len = ft_strlen(s);
		str = ft_strnew(len);
		str = ft_strcpy(str, s);
		while (str[i] != '\0')
		{
			str[i] = f(i, s[i]);
			i++;
		}
		return (str);
	}
	return (NULL);
}
