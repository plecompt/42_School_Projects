#include "libft.h"

int	ft_isdigit_str(char *str)
{
	int	i;

	if (str == NULL || str[0] == 0)
		return (0);
	i = (str[0] == '-' || str[0] == '+') ? 1 : 0;
	while (str[i] && str[i] >= '0' && str[i] <= '9')
		i++;
	if (str[i])
		return (0);
	return (1);
}
