#include "fdf.h"

int		couleur(t_env *e, double t)
{
	if (e->color_mod & 1)
		return (65536 * 127.5 * (cos(t) + 1) + (256 * 127.5 * (sin(t) + 1))
		+ (127.5 * (1 - cos(t))));
	return (646664);
}
